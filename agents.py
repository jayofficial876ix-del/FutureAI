def get_agent(agent):

    if agent == "chat":
        return "💬 Chat Agent"

    elif agent == "research":
        return "🔍 Research Agent"

    elif agent == "learn":
        return "📚 Learning Agent"

    elif agent == "code":
        return "💻 Coding Agent"

    elif agent == "website":
        return "🌐 Website Agent"

    elif agent == "image":
        return "🎨 Image Agent"

    elif agent == "brainstorm":
        return "💡 Brainstorm Agent"

    return "🤖 Future AI"